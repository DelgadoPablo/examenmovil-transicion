import 'package:flutter/cupertino.dart';

class Televisor {
  final int id;
  final String name;
  final String dimensiones;
  final String precio;
  final String modelo;
  final String especificaciones;

  Televisor({
    required this.id,
    required this.name,
    required this.dimensiones,
    required this.precio,
    required this.modelo,
    required this.especificaciones,
  });
}

